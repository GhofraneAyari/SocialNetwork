package models;

public class Page extends SNS {
    private int pageId;
    private String pageName;

    public int getPageId() {
        return pageId;
    }

    public void setPageId(int groupId) {
        this.pageId = groupId;
    }

    public String getPageName() {
        return pageName;
    }

    public void setPageName(String groupName) {
        this.pageName = groupName;
    }


}
